# Запрашиваем у пользователя скорость и время
v = float(input("Введите скорость (км/ч): "))
t = float(input("Введите время (часы): "))

# Общая длина дороги
road_length = 123

# Рассчитываем расстояние, которое проедет спортсмен
distance_covered = v * t

# Находим остаток от деления на длину дороги
stop_point = distance_covered % road_length

# Приводим результат к целому числу и выводим
print(int(stop_point))
