# Запрашиваем название футбольной команды
team_name = input("Введите название футбольной команды: ")

# Выводим сообщение
print(f"{team_name} - чемпион!")

# Рисуем черту длиной, равной длине названия команды
print("-" * len(team_name))

# Преобразуем строку в нижний регистр
team_name_lower = team_name.lower()

# Выводим длину названия команды
print(f"Длина названия команды: {len(team_name)}")

# Проверяем, есть ли в названии буква "п"
print(f"Буква 'п' в названии: {'п' in team_name_lower}")

# Считаем, сколько раз встречается буква "а"
print(f"Количество букв 'а': {team_name_lower.count('а')}")
