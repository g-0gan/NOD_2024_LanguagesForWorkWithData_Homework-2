R1 = float(input("Введите сопротивление первого проводника (в Омах): "))
R2 = float(input("Введите сопротивление второго проводника (в Омах): "))

# Вычисление общего сопротивления
R_total = R1 + R2

# Округление до одного знака после запятой
rounded_R_total = round(R_total, 1)

print("Общее сопротивление цепи:", rounded_R_total)
